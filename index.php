<section class="schedule">
    <p>In 2021 Maestro Enrique Diemecke will present master classes and workshops for orchestral conductors with the Orquesta Escuela Carlos Chávez of the Sistema Nacional de Fomento Musical of Mexico, focussing on orchestral romantic repertoire of the 19th Century, including works of Beethoven, Brahms, Wagner, Mendelssohn, Tchaikovsky, Dvorak, Sibelius, and Grieg.</p> 

    <div class="program_detail">
        <h2>Program details</h2>
        <p>Eight aspiring conductors will be selected as Full Participanting conductors in workshops and master clases with two pianos, rehearsals with the orchestra, one dress rehearsal and one public concert in the new Centro Cultural Los Pinos, located in the beautiful, historic Chapultepec Park in Mexico City. (September 6 to September 12, 2021.)</p>

        <p>In advance of the workshops and master clases, Maestro Diemecke will prepare the orchestra for one week in sectional rehearsals (August 23 to 29.) The following week he will rehearse the full orchestra and lead them in a concert of the repertoire selected for the master clases (August 30 – September 5.)</p>

        <p>The podium time for each Full Participating conductor will be as follows:</p>

        <ul>
            <li>Workshops with two pianos, totalling 55 minutes.</li>
            <li>Rehearsals with orchestra, totalling 75 minutes.</li>
            <li>Dress rehearsal, 11 minutes.</li>
            <li>Concert, work of 10 minutes to be assigned by Maestro Diemecke from the list below.</li>
        </ul>

        <p>Previous to the workshops and master classes in Mexico City, the Full Participanting conductors will receive a 3 hour daily webinar session with Maestro Diemecke (from August 30th to September 3rd), in preparation of the live sessions. </p>

        <p>Tuition for Full Participanting conductors includes shared hotel accommodations in a double room, breakfast for 8 days, and local transportation to and from all sessions.</p>

        <p>In addition to the 8 Full Participanting conductors, there will be 10 Auditor conductors selected, who will attend all of the sessions with the Full Participating conductors. In addition, Auditor conductors will have two 15 minute sessions with two pianos.</p>

        <p>All sessions will be professionally recorded by the organizer and offered to the participants.</p>
    </div>
</section>

